public class Kinetic {

    public double massaBenda;
    public double kecepatanBenda;
    public double Ek;

    public double getMassaBenda() {
        return massaBenda;
    }

    public void setMassaBenda(double massaBenda) {
        this.massaBenda = massaBenda;
    }

    public double getKecepatanBenda() {
        return kecepatanBenda;
    }

    public void setKecepatanBenda(double kecepatanBenda) {
        this.kecepatanBenda = kecepatanBenda;
    }

    public double getEk() {
        return Ek;
    }

    public void setEk(double Ek) {
        this.Ek = Ek;
    }
}
